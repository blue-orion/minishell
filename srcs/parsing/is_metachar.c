int	is_metachar(char c)
{
	if (c == '\'' || c == '\"' || c == '(' || c == ')'
		|| c == '|' || c == '&' || c == ';' || c == '$'
		|| c == '<' || c == '>' || c == ' ' || c == '\0')
		return (1);
	return (0);
}
